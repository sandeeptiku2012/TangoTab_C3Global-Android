package com.tangotab.core.vo;

public class DeviceResponse 
{
	private String responsCode;
	private String responseMssg;
	private String appStatus;
	private String tTL;
	/**
	 * @return the responsCode
	 */
	public String getResponsCode() {
		return responsCode;
	}
	/**
	 * @param responsCode the responsCode to set
	 */
	public void setResponsCode(String responsCode) {
		this.responsCode = responsCode;
	}
	/**
	 * @return the responseMssg
	 */
	public String getResponseMssg() {
		return responseMssg;
	}
	/**
	 * @param responseMssg the responseMssg to set
	 */
	public void setResponseMssg(String responseMssg) {
		this.responseMssg = responseMssg;
	}
	/**
	 * @return the appStatus
	 */
	public String getAppStatus() {
		return appStatus;
	}
	/**
	 * @param appStatus the appStatus to set
	 */
	public void setAppStatus(String appStatus) {
		this.appStatus = appStatus;
	}
	/**
	 * @return the tTL
	 */
	public String gettTL() {
		return tTL;
	}
	/**
	 * @param tTL the tTL to set
	 */
	public void settTL(String tTL) {
		this.tTL = tTL;
	}
	
	
}
